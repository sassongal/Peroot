<h2>שלב 1: להבין מה זה סוכן AI — ומה הוא לא</h2>
<p>לפני שבונים משהו, צריך אוצר מילים מדויק. שלושה מושגים מתבלבלים כל הזמן, ואי-הבחנה ביניהם היא הסיבה מספר אחת לפרויקטי אוטומציה שנכשלים או עולים יותר מדי.</p>

<h3>צ'אטבוט: קלט ← פלט</h3>
<p>צ'אטבוט מקבל הודעה ומחזיר תשובה. הוא לא מפעיל כלים חיצוניים, לא זוכר מטרה לאורך כמה צעדים, ולא פועל בעולם. שירות לקוחות שעונה על שאלות מתוך מאגר ידע הוא צ'אטבוט — מצוין, אבל לא סוכן.</p>

<h3>Workflow: צעדים קבועים בקוד</h3>
<p>ב-workflow, אתם (המפתחים) קובעים מראש את הרצף: "קבל טופס ← חלץ שדות ← בדוק תקינות ← שמור ב-CRM ← שלח מייל". המודל אולי מבצע צעד אחד או שניים (למשל חילוץ שדות), אבל <em>מהלך העבודה עצמו כתוב בקוד</em>. זה צפוי, ניתן לבדיקה, וזול. Anthropic מגדירים חמישה דפוסי workflow קלאסיים — שרשור פרומפטים, ניתוב, הרצה מקבילית, מתזמר-עובד, ומעריך-משפר — וכולם חולקים תכונה אחת: הבקרה נמצאת בקוד, לא במודל.</p>

<h3>Agent: המודל מנהל את המהלך</h3>
<p>בסוכן, אתם נותנים מטרה וארגז כלים — והמודל מחליט בעצמו מה לעשות בכל צעד, על סמך מה שראה עד עכשיו. "מצא את שלושת הספקים הזולים למוצר X, בדוק זמינות, והכן טבלת השוואה" — הסוכן יבחר מתי לחפש, מתי לגלוש, מתי לעצור. זו העוצמה (גמישות מול משימות פתוחות) וזו גם הסכנה (פחות צפיוּת). המדריך המעשי הצעד-אחר-צעד לבניית סוכן כזה נמצא במאמר הנפרד שלנו על <a href="/blog/agentic-ai-build-agents-no-code">בניית סוכני AI ללא קוד ב-Peroot</a>; כאן אנחנו מתמקדים במפה הרעיונית — מתי, למה, ואיזה דפוס.</p>

<h2>שלב 2: להכיר את לולאת הסוכן — perceive, reason, act, observe</h2>
<p>כל סוכן, פשוט ככל שיהיה, בנוי סביב לולאה אחת שחוזרת על עצמה:</p>
<ol dir="rtl">
  <li><strong>תפיסה (perceive):</strong> הסוכן קורא את המצב — בקשת המשתמש, תוצאות של צעד קודם, נתונים שהגיעו מכלי.</li>
  <li><strong>חשיבה (reason):</strong> המודל מחליט מה הצעד הבא — האם לענות, לחפש, לקרוא קובץ, או לקרוא לכלי מסוים עם פרמטרים מסוימים.</li>
  <li><strong>פעולה (act):</strong> הסוכן מפעיל כלי — API, שאילתת מסד נתונים, חיפוש אינטרנט, שליחת הודעה.</li>
  <li><strong>תצפית (observe):</strong> התוצאה חוזרת אל ההקשר, והלולאה מתחילה מחדש — עד שהמטרה הושגה או שמגיעים לגבול שהוגדר.</li>
</ol>

<h3>Tool use ו-function calling: איך המודל "לוחץ על כפתורים"</h3>
<p>מודל שפה לבדו רק מייצר טקסט. <strong>Function calling</strong> (או tool use) הוא המנגנון שמאפשר לו במקום זאת להחזיר בקשה מובנית — "הפעל את הכלי <code>get_weather</code> עם <code>city=חיפה</code>". הקוד שלכם מריץ את הכלי בפועל ומחזיר את התוצאה. זו התשובה לשאלה "האם וכיצד להפעיל כלי?". להגדרת ההתנהגות של המודל בתוך הלולאה — הטון, הגבולות, סדר העדיפויות — קראו את המדריך שלנו ל<a href="/blog/system-prompts-complete-guide">הנחיות מערכת (system prompts)</a>, כי סוכן טוב מתחיל בהנחיית מערכת מדויקת.</p>

<h3>MCP: שכבת החיבוריות</h3>
<p>אם function calling עונה על "האם להפעיל כלי", אז <strong>Model Context Protocol‏ (MCP)</strong> עונה על "איך מגיעים אל הכלי ומדברים איתו". MCP הוא תקן פתוח שמתקנן את הדרך שבה מודלים מתחברים לכלים ולמקורות מידע — במקום לכתוב אינטגרציה ייחודית לכל כלי. עד 2026 MCP הפך לתקן דה-פקטו: הוא אומץ על ידי Anthropic, OpenAI, Google, Microsoft ו-Amazon, חצה עשרות מיליוני הורדות SDK חודשיות, ויש כבר אלפי שרתי MCP ציבוריים. Anthropic אף תרמה את הפרוטוקול ל-Agentic AI Foundation תחת Linux Foundation, יחד עם Block ו-OpenAI. בפועל: MCP הוא מה שמחבר את הסוכן שלכם ל-Gmail, ל-Google Drive, ל-CRM או למסד הנתונים שלכם, בצורה סטנדרטית. הרחבנו על כך במדריך המלא ל<a href="/blog/mcp-model-context-protocol-guide">MCP — Model Context Protocol</a>.</p>

