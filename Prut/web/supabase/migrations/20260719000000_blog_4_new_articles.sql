<p dir="rtl">Anthropic מוסיפים את הזווית המשלימה: בעבודה עם extended thinking, "עדיף להעדיף הוראות כלליות על פני שלבים מוכתבים — פרומפט כמו 'תחשוב ביסודיות' לרוב מפיק נימוק טוב יותר מתוכנית שלב-אחר-שלב שכתב אדם, כי החשיבה של Claude תכופות עולה על מה שאדם היה מכתיב". כלומר: כשאתה כותב למודל את השלבים בעצמך, אתה מגביל אותו לרזולוציית החשיבה שלך — במקום לתת לו את שלו, שהיא לרוב עמוקה יותר.</p>

<h2 dir="rtl">מה כן לעשות: עקרונות לפרומפט למודל חשיבה</h2>

<h3 dir="rtl">1. הגדר מטרה, אילוצים וקריטריון הצלחה — לא נוהל</h3>

<p dir="rtl">התפקיד שלך הוא לומר <em>מה</em> אתה רוצה ו<em>באילו תנאים</em>, לא <em>איך</em> להגיע לשם. במקום "קרא את הטקסט, זהה את הבעיות, דרג אותן, ואז כתוב סיכום" — תן למודל את היעד, את המגבלות (אורך, פורמט, טון, מה אסור), ואת ההגדרה של "תשובה טובה". את שרשרת השלבים הוא יבנה בעצמו, ולרוב טוב יותר.</p>

<h3 dir="rtl">2. היה תמציתי ותן מקום</h3>

<p dir="rtl">מודלי חשיבה אוהבים פרומפטים נקיים. הצפה בהוראות מיקרו, אזהרות מיותרות ותזכורות חוזרות רק מוסיפה רעש. נסח ברור, קצר, וללא "תחכומי" הכוונה שכבר לא צריך. אם אתה עובד עם משימות ארוכות מאוד או הקשר עצום, כדאי גם להכיר את <a href="/blog/long-context-prompts-200k-tokens">כתיבת פרומפטים להקשר ארוך (‏200K טוקנים)</a> — מבנה ומיקום ההוראות משפיעים שם אף יותר.</p>

<h3 dir="rtl">3. הימנע מ‑few-shot chains שמכתיבים דרך פתרון</h3>

<p dir="rtl">אחד ההרגלים הכי מושרשים בהנדסת פרומפט קלאסית הוא לשים כמה דוגמאות פתורות (few-shot) לפני המשימה. במודלי חשיבה זה עלול להתהפך. התיעוד של DeepSeek‑R1 ממליץ במפורש להימנע מדוגמאות, כי "few-shot לרוב מוריד ביצועים — המודל מנסה לחקות את התבנית של הדוגמאות במקום להשתמש ביכולת החשיבה העדיפה שלו לפתור מאפס". ההמלצה: תיאור ברור ומפורט של הבעיה, המשימה ופורמט הפלט — ולתת למודל לחשוב zero-shot. (אם בכל זאת צריך דוגמאות אצל Claude, Anthropic ממליצים לעטוף את הנימוק בדוגמה בתגיות <code>thinking</code>, כדי להדגים סגנון ולא לכפות שלבים.)</p>

<h2 dir="rtl">בקרות מאמץ, אורך ותקציב חשיבה</h2>

<p dir="rtl">הנה הבשורה המשמחת: את כל ה"כוונון העדין" שפעם ניסינו להשיג בטריקים לשוניים, היום מקבלים דרך פרמטרים ייעודיים. במקום לכתוב "תחשוב ממש לעומק" — פשוט מעלים את רמת המאמץ.</p>

<ul dir="rtl">
<li><strong>OpenAI GPT-5 / o-series:</strong> פרמטר <code>reasoning_effort</code> עם הערכים <em>minimal / low / medium / high</em> שולט בכמות טוקני החשיבה, ופרמטר <code>verbosity</code> (‏low/medium/high) שולט באורך התשובה הסופית — בנפרד מ‑<code>max_tokens</code>.</li>
<li><strong>Anthropic Claude:</strong> extended thinking עם ארבע רמות effort — <em>low, medium, high (ברירת מחדל), max</em> — וכן <em>adaptive thinking</em>, שבו המודל מחליט לבד מתי כדאי לחשוב לעומק. תקציב החשיבה המינימלי הוא 1,024 טוקנים; ההמלצה היא להתחיל נמוך ולעלות בהדרגה.</li>
<li><strong>Google Gemini 2.5:</strong> <code>thinking budget</code> נפרד, ברירת מחדל Auto, בטווח 0–24,576 טוקנים ב‑Flash. ההמלצה המעשית: להתחיל סביב 8,000 ולהעלות רק אם האיכות עקבית ונמוכה — כי יותר תקציב לא אומר אוטומטית תשובה טובה יותר.</li>
</ul>

<p dir="rtl">שים לב לדפוס החוזר: <strong>יותר חשיבה אינה תמיד עדיפה</strong>. יש תשואה פוחתת, ולעיתים אף ירידה באיכות ובמהירות. התחל נמוך, מדוד, והעלה רק כשצריך.</p>

<h2 dir="rtl">ההבדלים בין המודלים — מה כל ספק מבקש ממך</h2>

<p dir="rtl">גם בתוך מחלקת מודלי החשיבה יש ניואנסים חשובים. השוואה רחבה בין דורות המודלים תמצא ב<a href="/blog/gpt-5-vs-claude-opus-4-hebrew-2026">מדריך GPT-5 מול Claude Opus 4</a>; כאן נתמקד בהשלכות על הפרומפט:</p>

<ul dir="rtl">
<li><strong>DeepSeek‑R1:</strong> הכי רגיש. ממליצים <em>לא</em> להשתמש ב‑system prompt — כל ההוראות צריכות להיות ב‑user prompt. להימנע מ‑few-shot, ולהחזיק טמפרטורה יציבה סביב 0.5–0.7 (‏0.6 הוא הנקודה המתוקה); טמפרטורה גבוהה מדי "שוברת" את שרשרת הנימוק.</li>
<li><strong>OpenAI o-series:</strong> מגיב מצוין להוראות קצרות וממוקדות. לא לבקש נימוק נוסף לפני קריאות כלים; לתת מטרה ואילוצים, ולתת לו לתכנן.</li>
<li><strong>Claude (extended thinking):</strong> אוהב הוראות כלליות ("תחשוב ביסודיות") על פני שלבים מוכתבים; תומך ב‑interleaved thinking בין קריאות כלים; חשוב להחזיר את בלוקי החשיבה בשלמותם ל‑API כשעובדים עם כלים.</li>
<li><strong>Gemini 2.5 Thinking:</strong> נשלט בעיקר דרך thinking budget; הוראות "שלב-שלב" מבזבזות אותו.</li>
</ul>

