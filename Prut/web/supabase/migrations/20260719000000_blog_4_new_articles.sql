החזר: (1) השורה הבעייתית, (2) התיקון, (3) למה זה קרה."</code></pre>

<p dir="rtl">שים לב שבגרסת ה"אחרי" הורדנו כל הנחיית "תחשוב לאט/שלב-שלב", והוספנו במקום זה <em>קריטריון הצלחה מדויק</em>. זה בדיוק המעבר. לעקרונות הרחבים של ניסוח פרומפט טוב — בכל סוג מודל — ראה את <a href="/blog/how-to-write-good-prompt">המדריך לכתיבת פרומפט טוב</a>.</p>

<h2 dir="rtl">שאלות נפוצות</h2>

<h3 dir="rtl">האם אסור לחלוטין לכתוב "תחשוב שלב-שלב" למודל חשיבה?</h3>
<p dir="rtl">לא "אסור", אבל זה בזבזני ולעיתים מזיק. ההנחיות הרשמיות של OpenAI ו‑Google מציינות שהבקשה מיותרת כי המודל כבר עושה זאת בפנים, ושהיא עלולה לבזבז תקציב חשיבה או לפגוע בביצועים. במקום זה — נסח מטרה ואילוצים ברורים, ותן למודל לחשוב לבד.</p>

<h3 dir="rtl">אם מודלי חשיבה חזקים יותר, למה לא להשתמש בהם תמיד?</h3>
<p dir="rtl">כי הם איטיים ויקרים יותר. כל טוקן חשיבה עולה זמן וכסף, ובמשימות פשוטות (סיכום, סיווג, מענה קצר) מודל חשיבה לא ישפר את התוצאה אבל כן יגדיל עלות ולטנציה. למשימות "קלות" מודל רגיל או מודל חשיבה במאמץ נמוך עדיף.</p>

<h3 dir="rtl">מה ההבדל בין reasoning_effort ל‑verbosity ב‑GPT-5?</h3>
<p dir="rtl">‏<code>reasoning_effort</code> קובע כמה המודל <em>חושב</em> בפנים לפני שהוא עונה (minimal/low/medium/high). ‏<code>verbosity</code> קובע כמה <em>ארוכה</em> התשובה הסופית (low/medium/high). הם בלתי תלויים: אפשר מאמץ חשיבה גבוה עם תשובה קצרה, ולהפך. הוראה מפורשת בפרומפט (למשל "כתוב חמש פסקאות") גוברת על הגדרת ה‑verbosity.</p>

<h3 dir="rtl">האם few-shot תמיד מזיק במודלי חשיבה?</h3>
<p dir="rtl">לא תמיד, אבל היזהר. ב‑DeepSeek‑R1 ההמלצה הרשמית היא להימנע מדוגמאות כי הן מורידות ביצועים. ב‑Claude אפשר להשתמש בדוגמאות אם עוטפים את הנימוק בתגיות <code>thinking</code> כדי להדגים סגנון ולא לכפות שלבים. הכלל: אם הדוגמה מכתיבה <em>דרך פתרון</em>, סביר שהיא תזיק; אם היא מדגימה רק <em>פורמט פלט</em>, לרוב היא בטוחה.</p>

<h3 dir="rtl">איך אני בוחר תקציב חשיבה (thinking budget) נכון?</h3>
<p dir="rtl">התחל נמוך ועלה בהדרגה. Anthropic ממליצים להתחיל מהמינימום (1,024 טוקנים) ולעלות; Google ממליצים להתחיל סביב 8,000 ב‑Gemini Flash ולהעלות רק אם האיכות עקבית ונמוכה. אין קסם ב"תקציב גבוה" — יש תשואה פוחתת, ולעיתים גם ירידה באיכות.</p>

