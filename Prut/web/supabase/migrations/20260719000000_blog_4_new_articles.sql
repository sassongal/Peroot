<h2 dir="rtl">איך מגדירים לפרומפט לכתוב עברית טבעית</h2>
<p dir="rtl">רוב האנשים כותבים ״כתוב לי פוסט על X בעברית״ ומתפלאים שיוצא רובוטי. הבעיה היא שלא נתתם למודל מספיק כדי לדעת <em>איך</em> לכתוב. עברית טבעית מתחילה מהוראות מדויקות, בדיוק כמו כל פרומפט טוב. אם אתם עוד לא בטוחים בבסיס, שווה לעבור על <a href="/blog/how-to-write-good-prompt">המדריך לכתיבת פרומפט טוב</a>, כי כל מה שכאן נבנה עליו.</p>

<h3 dir="rtl">1. הגדירו קהל, משלב וקול</h3>
<p dir="rtl">״קהל״ זה מי קורא. ״משלב״ זה כמה רשמי. ״קול״ זה מי מדבר. שלושתם ביחד קובעים את הטון. ככל שתפרטו, כך המודל יתרחק מברירת המחדל האפורה שלו.</p>
<pre dir="rtl"><code>כתוב פוסט קצר על ניהול זמן.
הקהל: הורים עובדים בני 30-45, קוראים בטלפון בין משימה למשימה.
משלב: יומיומי-חברי, כמו שיחה בין חברים, לא הרצאה.
קול: מישהי שעברה את זה בעצמה ומדברת מניסיון, לא יועצת.
פנייה: ברבים (״תנסו״, ״כדאי לכם״), לא בזכר יחיד.</code></pre>

<h3 dir="rtl">2. תנו דוגמת סגנון לחיקוי</h3>
<p dir="rtl">זה הטריק החזק ביותר, בלי תחרות. שום תיאור מופשט לא שווה פסקה אמיתית שכתבתם בעצמכם. הדביקו קטע קצר בסגנון שאתם אוהבים ובקשו מהמודל לחקות את הקצב, אורך המשפטים ובחירת המילים, לא את התוכן.</p>
<pre dir="rtl"><code>הנה פסקה בסגנון שאני אוהב:
"[הדביקו כאן 4-5 שורות שכתבתם, או קטע מבלוגר שאתם מחבבים]"

כתוב את הטקסט החדש באותו קצב ואותו אוצר מילים.
שים לב במיוחד לאורך המשפטים ולמעברים ביניהם.</code></pre>

<h3 dir="rtl">3. אסרו קלישאות בשמן המפורש</h3>
<p dir="rtl">מודלים מצייתים לאיסורים מפורשים הרבה יותר טוב מאשר להנחיות עמומות. אל תגידו ״כתוב טבעי״, תגידו בדיוק ממה להימנע. רשימת ההרחקה הזו לבדה משדרגת כל טקסט:</p>
<pre dir="rtl"><code>אל תשתמש ב: "מהווה", "ראוי לציין", "בעולם של היום",
"למנף", "ערך מוסף", "בשורה התחתונה", "המסע שלך".
אל תשתמש בתבנית "לא רק X אלא גם Y".
אל תפתח שלוש פסקאות באותה מילה.
ערבב אורכי משפטים: שלב משפט קצר של 3-4 מילים
בין משפטים ארוכים.
כתוב בקול פעיל, לא סביל.</code></pre>

<h3 dir="rtl">4. בקשו ניב ישראלי, במידה</h3>
<p dir="rtl">כשהמשלב מתאים, ביטוי ישראלי אחד לפסקה עושה את ההבדל בין תרגום למקור. ״תכלס״, ״אין מצב״, ״שווה את זה״, אלה דברים שאדם אומר ומכונה נמנעת מהם. אבל זהירות: אל תבקשו ״הרבה סלנג״, כי אז יוצא מאולץ בכיוון ההפוך. אחד או שניים, במקום הנכון, וזהו.</p>

<h3 dir="rtl">5. תקנו באיטרציה, לא בפרומפט אחד</h3>
<p dir="rtl">גם הפרומפט הכי טוב לא ייתן טקסט מושלם במכה ראשונה, וזה בסדר. הכוח האמיתי הוא בעריכה חוזרת. במקום לנסח מחדש מאפס, הצביעו על מה שצורם ובקשו תיקון ממוקד:</p>
<pre dir="rtl"><code>ערוך את הטקסט שיישמע יותר כמו שיחה ופחות כמו הרצאה.
תוריד את המשפט השני, הוא נשמע מתורגם.
תשבור את הפסקה הארוכה לשתיים, עם משפט קצר באמצע.
תחליף כל "מהווה" ב"הוא" או "זה".</code></pre>
<p dir="rtl">אם אתם עובדים עם קול קבוע, של עסק, מותג או פרסונה, כדאי לשמור את חבילת ההוראות הזו ולעשות בה שימוש חוזר. בדיוק בשביל זה בנינו את <a href="/library">ספריית הפרומפטים של Peroot</a>: פרומפט אחד מלוטש שעובד, במקום להמציא את הגלגל בכל פעם.</p>

<h2 dir="rtl">לפני ואחרי — איך נראה התיקון בפועל</h2>
<p dir="rtl">תיאוריה זה נחמד, אבל ההבדל מורגש רק כשרואים אותו. הנה שני מקרים אמיתיים, פסקה שיווקית ופסקה מקצועית, לפני ואחרי הטיפול.</p>

<h3 dir="rtl">דוגמה 1 — טקסט שיווקי</h3>
<blockquote dir="rtl">
  <p><strong>לפני (AI מובהק):</strong> בעולם המהיר והמשתנה של היום, פתרונות ניהול משימות מהווים אבן דרך משמעותית עבור צוותים השואפים להתייעל. ראוי לציין כי הכלי החדשני, הפשוט והיעיל שלנו, אשר ממנף בינה מלאכותית מתקדמת, מציע חוויית משתמש מיטבית. אתה תוכל להבין שזה לא רק כלי, זו מהפכה. המסע שלך להצלחה מתחיל כאן.</p>
</blockquote>
<blockquote dir="rtl">
  <p><strong>אחרי (אנושי):</strong> צוותים מבזבזים שעות על תיאום משימות במקום על העבודה עצמה. בנינו כלי שעושה את התיאום במקומכם: הוא מזהה מה תקוע, מי צריך לזוז, ומתי. תכלס, פחות פגישות סטטוס ויותר דברים שנסגרים. תנסו שבועיים ותרגישו את ההבדל.</p>
</blockquote>
<p dir="rtl">מה קרה כאן? נמחקו ״בעולם של היום״, ״מהווים״, ״ראוי לציין״, ״ממנף״, ״חוויית משתמש מיטבית״, ״אשר״, ״לא רק... זו״ ו״המסע שלך״. במקומם נכנסו בעיה קונקרטית, פועל פעיל, מספר (״שבועיים״) וסלנג אחד (״תכלס״). האורך התקצר בחצי, והמסר התחדד.</p>

<h3 dir="rtl">דוגמה 2 — טקסט מקצועי</h3>
<blockquote dir="rtl">
  <p><strong>לפני:</strong> יש לציין כי התהליך מתאפיין ביעילות גבוהה. ההחלטה על אופן היישום התקבלה על ידי הצוות תוך התחשבות בכלל הגורמים הרלוונטיים, באופן שמבטיח תוצאות אופטימליות לאורך זמן.</p>
</blockquote>
<blockquote dir="rtl">
  <p><strong>אחרי:</strong> התהליך יעיל. הצוות בחר איך ליישם אותו אחרי ששקל את כל השיקולים, וזה מה שמחזיק לאורך זמן.</p>
</blockquote>
<p dir="rtl">כאן התיקון קטן אבל חד: הסביל (״התקבלה על ידי הצוות״) הפך לפעיל (״הצוות בחר״), ״מתאפיין ביעילות״ הפך ל״יעיל״, וכל הסייגים המנופחים נשרו. אותו מידע, חצי מהמילים, וקול של בן אדם.</p>

<h2 dir="rtl">RTL, פיסוק וניקוד — התקלות שלא רואים</h2>
<p dir="rtl">חלק מהסימנים אינם במילים אלא בתווים עצמם, והם שוברים פורמט ב-Word, ב-WhatsApp וב-CMS בלי שתבינו למה. שווה להכיר אותם, כי הם מהתיקונים הכי מהירים.</p>
<ul dir="rtl">
  <li><strong>מרכאות מסולסלות (״ ״ הלועזיות):</strong> מודלים מכניסים לפעמים מרכאות פתיחה וסגירה בסגנון אנגלי, שנראות שגוי בעברית. החליפו למרכאות ישרות או לגרשיים עבריים, ובעיקר היו עקביים.</li>
  <li><strong>שימוש יתר בקו מפריד:</strong> AI אוהב לפזר את הקו הארוך בכל מקום, כתחליף לפסיק או נקודתיים. בעברית הוא לגיטימי במינון קטן; כשהוא חוזר בכל פסקה, הוא הופך לסימן היכר.</li>
  <li><strong>תווי כיוון נסתרים:</strong> לפעמים נשתלים סימני RLM/LRM בלתי נראים שמבלבלים את הכיווניות. הם המקור הנפוץ ל״למה הנקודה קפצה לצד השני של המשפט״.</li>
