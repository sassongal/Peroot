<p dir="rtl">מודל חשיבה חזק יותר, אך גם איטי ויקר יותר (יותר טוקני חשיבה = יותר עלות ולטנציה). הכלל הפשוט: <strong>מודל חשיבה למשימות שבהן הדרך לתשובה קשה; מודל רגיל למשימות שבהן התשובה פשוט צריכה להיכתב.</strong></p>

