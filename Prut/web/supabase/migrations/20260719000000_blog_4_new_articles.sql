<h2 dir="rtl">למה עברית של AI נשמעת מזויפת</h2>
<p dir="rtl">הבעיה כמעט אף פעם אינה דקדוק. מודלים כמו ChatGPT, Claude ו-Gemini כותבים עברית תקנית להפליא, ובכל זאת אוזן ישראלית מזהה אותם בשנייה. הסיבה היא <strong>תרגומית</strong>: המודל חושב בדפוסים אנגליים ומתרגם אותם, אז מקבלים משפט עברי נכון שיושב על שלד זר.</p>
<p dir="rtl">קחו את המילה ״מהווה״. באנגלית יש פועל קופולה גמיש, <em>constitutes / represents</em>, והמודל מתרגם אותו הכי הרבה פעמים ל״מהווה״. ישראלי יגיד פשוט ״הוא״, ״זה״, או ״יש לו״. אותו דבר עם ״לא רק... אלא גם״, תבנית ישירה של <em>It's not just X, it's Y</em> שכמעט לא קיימת בדיבור עברי טבעי. ברגע שאתם שמים לב לזה, אי אפשר להפסיק לראות את זה.</p>

<h3 dir="rtl">הקצב האחיד</h3>
<p dir="rtl">בני אדם כותבים בקצב מעורב. משפט ארוך ומתפתל, ואז אחד קצר. פאוזה. AI, לעומת זאת, נוטה למשפטים באורך דומה מדי, בזה אחר זה, כמו שורה של חיילים. הטקסט יוצא חלק אבל שטוח, בלי נשימה. זה אחד ההבדלים שהכי קשה להסביר וגם הכי קל להרגיש.</p>

<h3 dir="rtl">החיבורים החוזרים</h3>
<p dir="rtl">שימו לב לחיבורים. AI נשען שוב ושוב על אותה קומץ מילות קישור רשמיות: ״כמו כן״, ״יתרה מכך״, ״בנוסף לכך״, ״במסגרת זו״, ״לפיכך״. אדם מגוון: לפעמים ״וגם״, לפעמים ״חוץ מזה״, ולפעמים בכלל מתחיל משפט חדש בלי חיבור. הצפיפות של החיבורים ה״נכונים״ מדי היא בעצמה תמרור. אותו דבר עם <em>ניסוח גנרי</em>: ״פתרון מקיף״, ״כלי עוצמתי״, ״מגוון רחב של אפשרויות״. אלה מילים שנשמעות טוב ולא אומרות כלום. ברגע שמחליפים אותן בפרט קונקרטי, ״חוסך שעתיים בשבוע״ במקום ״עוצמתי״, הטקסט מפסיק לרחף ומתחיל לדבר.</p>

<h3 dir="rtl">המשלב הלא נכון</h3>
<p dir="rtl">המודל נוטה למשלב גבוה ואחיד. הוא יכתוב פוסט לאינסטגרם באותה רשמיות שבה יכתוב מכתב לרשות המסים. ישראלים מחליפים משלב כל הזמן, קצת סלנג פה, מונח מקצועי שם, וכשהכל באותו גובה זה מרגיש כמו הודעה אוטומטית ולא כמו בן אדם.</p>

