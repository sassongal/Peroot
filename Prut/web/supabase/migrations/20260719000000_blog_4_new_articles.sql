<h2 dir="rtl">דוגמאות לפני / אחרי בעברית</h2>

<p dir="rtl">הנה איך נראה מעבר מפרומפט "בסגנון ישן" לפרומפט שמתאים למודל חשיבה.</p>

<p dir="rtl"><strong>דוגמה 1 — ניתוח החלטה עסקית</strong></p>

<pre dir="rtl"><code>לפני (סגנון מודל רגיל):
"בוא נחשוב על זה צעד אחר צעד. קודם רשום את כל האפשרויות,
אחר כך נתח כל אחת, אחר כך שקלל, ואז תן המלצה. תחשוב לאט
